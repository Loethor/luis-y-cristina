function getLang() {
  const params = new URLSearchParams(window.location.search);
  const urlLang = params.get('lang');
  if (urlLang && SUPPORTED_LANGS.includes(urlLang)) {
    localStorage.setItem('lang', urlLang);
    return urlLang;
  }
  const stored = localStorage.getItem('lang');
  if (stored && SUPPORTED_LANGS.includes(stored)) return stored;
  return DEFAULT_LANG;
}

function applyTranslations(t, lang) {
  document.documentElement.lang = lang;

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key] !== undefined) el.innerHTML = t[key];
  });

  document.querySelectorAll('[data-i18n-alt]').forEach(el => {
    const key = el.getAttribute('data-i18n-alt');
    if (t[key] !== undefined) el.alt = t[key];
  });

  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    const key = el.getAttribute('data-i18n-title');
    if (t[key] !== undefined) document.title = t[key];
  });

  // Update active language switcher link
  document.querySelectorAll('.language-switcher a').forEach(a => {
    const aLang = new URLSearchParams(new URL(a.href, location.href).search).get('lang') || DEFAULT_LANG;
    a.classList.toggle('active', aLang === lang);
  });

  // Update back link to preserve language
  const backLink = document.querySelector('[data-i18n="back_link"]');
  if (backLink) {
    backLink.href = 'index.html?lang=' + lang;
  }

  // Update info card links to preserve language
  document.querySelectorAll('a[href^="venue.html"], a[href^="tourism.html"], a[href^="accommodation.html"]').forEach(a => {
    const base = a.getAttribute('href').split('?')[0];
    a.href = base + '?lang=' + lang;
  });
}

function initI18n() {
  const lang = getLang();
  const t = TRANSLATIONS[lang] || TRANSLATIONS[DEFAULT_LANG];
  applyTranslations(t, lang);
}

initI18n();
